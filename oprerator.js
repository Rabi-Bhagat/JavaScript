//increment and decrement
let s = 1;
console.log(s++); // Output: 1 (post-increment: returns the value before incrementing).
console.log(s);// here is the value of s is 2 because it is increment from the s++ from above line.
console.log(++s); // Output: 3 (pre-increment: increments first, then returns the value)
console.log("here is the decrement:-");

